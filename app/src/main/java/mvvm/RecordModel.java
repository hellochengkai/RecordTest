package mvvm;

public class RecordModel {

    private long time;
    private long len;

    public RecordModel() {
    }

    public long getTime() {
        return time;
    }

    public long getLen() {
        return len;
    }

    public void setTime(long time) {
        this.time = time;
    }

    public void setLen(long len) {
        this.len = len;
    }
}
